#Board Indexes and Configuration

P1_PITS = list(range(0, 6))
P1_STORE = 6
P2_PITS = list(range(7, 13))
P2_STORE = 13

class MancalaBoard:
    def __init__(self):
        self.board = [4, 4, 4, 4, 4, 4, 0, 4, 4, 4, 4, 4, 4, 0]
        self.current_player = 1
        self.extra_move_earned = False
        self.stones_captured = 0

    def is_game_over(self):
        p1_empty = all(self.board[i] == 0 for i in P1_PITS)
        p2_empty = all(self.board[i] == 0 for i in P2_PITS)
        return p1_empty or p2_empty

    def clean_up_board(self):
        p1_rem = sum(self.board[i] for i in P1_PITS)
        p2_rem = sum(self.board[i] for i in P2_PITS)
        self.board[P1_STORE] += p1_rem
        self.board[P2_STORE] += p2_rem
        for i in P1_PITS + P2_PITS:
            self.board[i] = 0

    def get_valid_moves(self, player):
        pits = P1_PITS if player == 1 else P2_PITS
        return [i for i in pits if self.board[i] > 0]

    def make_move(self, pit_index):
        stones = self.board[pit_index]
        self.board[pit_index] = 0
        current_idx = pit_index
        player = self.current_player
        self.extra_move_earned = False
        self.stones_captured = 0

        while stones > 0:
            current_idx = (current_idx + 1) % 14
            if player == 1 and current_idx == P2_STORE:
                continue
            if player == 2 and current_idx == P1_STORE:
                continue
            self.board[current_idx] += 1
            stones -= 1

        if player == 1 and current_idx == P1_STORE:
            self.extra_move_earned = True
        elif player == 2 and current_idx == P2_STORE:
            self.extra_move_earned = True

        my_pits = P1_PITS if player == 1 else P2_PITS
        my_store = P1_STORE if player == 1 else P2_STORE

        if current_idx in my_pits and self.board[current_idx] == 1:
            opposite_idx = 12 - current_idx
            if self.board[opposite_idx] > 0:
                captured = self.board[current_idx] + self.board[opposite_idx]
                self.board[my_store] += captured
                self.stones_captured = captured
                self.board[current_idx] = 0
                self.board[opposite_idx] = 0

        if not self.extra_move_earned:
            self.current_player = 2 if player == 1 else 1

        if self.is_game_over():
            self.clean_up_board()

#AI Logic (Minimax Alpha-Beta)
def evaluate_heuristic(board_state, player, heuristic_type, W1=2, W2=1, W3=3, W4=4):
    my_store = board_state.board[P1_STORE] if player == 1 else board_state.board[P2_STORE]
    opp_store = board_state.board[P2_STORE] if player == 1 else board_state.board[P1_STORE]
    my_side_stones = sum(board_state.board[i] for i in (P1_PITS if player == 1 else P2_PITS))
    opp_side_stones = sum(board_state.board[i] for i in (P2_PITS if player == 1 else P1_PITS))
    h1 = my_store - opp_store
    if heuristic_type == 1: return h1
    h2 = W1 * h1 + W2 * (my_side_stones - opp_side_stones)
    if heuristic_type == 2: return h2
    add_move = 1 if board_state.extra_move_earned else 0
    h3 = h2 + W3 * add_move
    if heuristic_type == 3: return h3
    return h3 + W4 * board_state.stones_captured

def minimax_alpha_beta(board_state, depth, alpha, beta, maximizing_player, player, heuristic_type):
    if depth == 0 or board_state.is_game_over():
        return evaluate_heuristic(board_state, player, heuristic_type), None
    valid_moves = board_state.get_valid_moves(board_state.current_player)
    if not valid_moves:
        next_state = copy.deepcopy(board_state)
        next_state.current_player = 2 if next_state.current_player == 1 else 1
        return minimax_alpha_beta(next_state, depth - 1, alpha, beta, not maximizing_player, player, heuristic_type)[0], None
    best_move = random.choice(valid_moves) if valid_moves else None
    if maximizing_player:
        max_eval = float('-inf')
        for move in valid_moves:
            next_state = copy.deepcopy(board_state)
            next_state.make_move(move)
            is_max = maximizing_player if next_state.current_player == board_state.current_player else not maximizing_player
            evaluation, _ = minimax_alpha_beta(next_state, depth - 1, alpha, beta, is_max, player, heuristic_type)
            if evaluation > max_eval:
                max_eval = evaluation
                best_move = move
            alpha = max(alpha, evaluation)
            if beta <= alpha: break
        return max_eval, best_move
    else:
        min_eval = float('inf')
        for move in valid_moves:
            next_state = copy.deepcopy(board_state)
            next_state.make_move(move)
            is_max = maximizing_player if next_state.current_player == board_state.current_player else not maximizing_player
            evaluation, _ = minimax_alpha_beta(next_state, depth - 1, alpha, beta, is_max, player, heuristic_type)
            if evaluation < min_eval:
                min_eval = evaluation
                best_move = move
            beta = min(beta, evaluation)
            if beta <= alpha: break
        return min_eval, best_move


