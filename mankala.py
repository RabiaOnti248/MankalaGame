# Graphical User Interface (GUI)

class MancalaGUI:
    def __init__(self, ai_heuristic=4, ai_depth=4):
        self.game = MancalaBoard()
        self.ai_heuristic = ai_heuristic
        self.ai_depth = ai_depth
        self.output = widgets.Output()

        # Creating a wider wrapper layout for the status bar

        self.status_label = widgets.Label(value="🎮 Game Started! Your Turn (Player 1)")
        self.status_label.add_class("status-text")

        self.status_box = widgets.HBox([self.status_label], layout=widgets.Layout(width='100%'))
        self.status_box.add_class("status-container")

        self.create_board_widgets()
        self.update_gui()

    def get_marbles_text(self, count):
        if count == 0:
            return "Empty"
        elif count <= 4:
            return "🔵" * count + f"\n({count})"
        else:
            return "🔵" * 4 + f"..\n({count})"

    def create_board_widgets(self):
        self.p2_store_btn = widgets.Button(description="", disabled=True, layout=widgets.Layout(width='100px', height='190px'))
        self.p2_store_btn.add_class('mancala-store')

        self.p1_store_btn = widgets.Button(description="", disabled=True, layout=widgets.Layout(width='100px', height='190px'))
        self.p1_store_btn.add_class('mancala-store')

        self.p2_buttons = []
        for idx in reversed(P2_PITS):
            btn = widgets.Button(description="", disabled=True, layout=widgets.Layout(width='85px', height='110px', margin='5px'))
            btn.add_class('mancala-pit')
            self.p2_buttons.append(btn)

        self.p1_buttons = []
        for idx in P1_PITS:
            btn = widgets.Button(description="", layout=widgets.Layout(width='85px', height='110px', margin='5px'))
            btn.add_class('mancala-pit')
            btn.on_click(self.get_click_handler(idx))
            self.p1_buttons.append(btn)

        p2_row = widgets.HBox(self.p2_buttons)
        p1_row = widgets.HBox(self.p1_buttons)

        p2_labels = []
        for i in reversed(P2_PITS):
            lbl = widgets.Label(value=f"Pit {i}", layout=widgets.Layout(width='85px', justify_content='center', margin='0 5px'))
            lbl.add_class('pit-label-style')
            p2_labels.append(lbl)

        p1_labels = []
        for i in P1_PITS:
            lbl = widgets.Label(value=f"Pit {i}", layout=widgets.Layout(width='85px', justify_content='center', margin='0 5px'))
            lbl.add_class('pit-label-style')
            p1_labels.append(lbl)

        p2_labels_hbox = widgets.HBox(p2_labels)
        p1_labels_hbox = widgets.HBox(p1_labels)

        middle_pits = widgets.VBox([p2_labels_hbox, p2_row, p1_row, p1_labels_hbox], layout=widgets.Layout(align_items='center'))

        ai_store_lbl = widgets.Label(value="🟡 AI Store")
        ai_store_lbl.add_class('store-title-style')

        your_store_lbl = widgets.Label(value="🔵 Your Store")
        your_store_lbl.add_class('store-title-style')

        self.board_layout = widgets.HBox([
            widgets.VBox([ai_store_lbl, self.p2_store_btn], layout=widgets.Layout(align_items='center')),
            middle_pits,
            widgets.VBox([your_store_lbl, self.p1_store_btn], layout=widgets.Layout(align_items='center'))
        ], layout=widgets.Layout(align_items='center', justify_content='center'))
        self.board_layout.add_class('mancala-board')

    def update_gui(self):
        self.p2_store_btn.description = f"🟡 x {self.game.board[13]}" if self.game.board[13] > 0 else "0"
        self.p1_store_btn.description = f"🔵 x {self.game.board[6]}" if self.game.board[6] > 0 else "0"

        for i, idx in enumerate(reversed(P2_PITS)):
            count = self.game.board[idx]
            self.p2_buttons[i].description = "🟡" * count + f"\n({count})" if count <= 4 else "🟡" * 4 + f"..\n({count})" if count > 0 else "Empty"

        valid_moves = self.game.get_valid_moves(1)
        for i, idx in enumerate(P1_PITS):
            count = self.game.board[idx]
            self.p1_buttons[i].description = "🔵" * count + f"\n({count})" if count <= 4 else "🔵" * 4 + f"..\n({count})" if count > 0 else "Empty"

            if self.game.is_game_over() or self.game.current_player != 1 or idx not in valid_moves:
                self.p1_buttons[i].disabled = True
            else:
                self.p1_buttons[i].disabled = False

    def get_click_handler(self, pit_idx):
        def on_button_clicked(b):
            if self.game.current_player == 1 and not self.game.is_game_over():
                self.game.make_move(pit_idx)
                self.update_gui()

                if self.game.is_game_over():
                    self.check_winner()
                    return

                if self.game.current_player == 1:
                    self.status_label.value = "🎉 Extra Move! You earned another turn."
                else:
                    self.status_label.value = "🤖 AI is thinking..."
                    self.ai_turn()
        return on_button_clicked

    def ai_turn(self):
        while self.game.current_player == 2 and not self.game.is_game_over():
            time.sleep(0.8)
            _, move = minimax_alpha_beta(self.game, self.ai_depth, float('-inf'), float('+inf'), True, 2, self.ai_heuristic)
            if move is not None:
                self.game.make_move(move)
                self.update_gui()
                if self.game.extra_move_earned and not self.game.is_game_over():
                    self.status_label.value = f"🤖 AI chose Pit {move} and earned an Extra Move!"
                else:
                    self.status_label.value = f"🤖 AI chose Pit {move}. Now it's your turn!"
            else:
                break
        if self.game.is_game_over():
            self.check_winner()

    def check_winner(self):
        self.update_gui()
        p1_score = self.game.board[P1_STORE]
        p2_score = self.game.board[P2_STORE]
        result_text = f"🏆 Game Over! Your Score: {p1_score} | AI Score: {p2_score}. "
        if p1_score > p2_score: result_text += "🎉 You Win!"
        elif p2_score > p1_score: result_text += "🤖 AI Wins!"
        else: result_text += "🤝 It's a Tie!"
        self.status_label.value = result_text

    def display(self):
        with self.output:
            display(widgets.VBox([self.status_box, widgets.Label(value=""), self.board_layout]))
        display(self.output)

